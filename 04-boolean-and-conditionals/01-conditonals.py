# conditional logic using 'if' statements represents different paths a program can take
# based on some type of comparison of input

color = input('what is your favorite color? ')
if color == 'purple':
    print('excellent choice!')
elif color == 'teal':
    print('not bad')
elif color == 'seafoam':
    print('mediocre')
elif color == 'red':
    print('i like how you think')
else:
    print('you monster!')