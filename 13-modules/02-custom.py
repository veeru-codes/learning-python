# custom modules
# - you can create your own custom modules by creating a .py file and defining functions, classes, and variables in it
# - you can then import the module and use its functions, classes, and variables in your code

# import the custom module and use its functions and variables
import my_module

print(my_module.greet("Alice"))  # prints "Hello, Alice!"
print(my_module.add(5, 3))  # prints 8
print(my_module.divide(10, 2))  # prints 5.0
print(my_module.PI)  # prints 3.14159
print(my_module.colorize("Hello, World!", "red"))  # prints "red: Hello, World!"